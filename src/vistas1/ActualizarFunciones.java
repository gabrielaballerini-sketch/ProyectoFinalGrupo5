    private void armarTabla(){

    modelo.addColumn("idFuncion");
    modelo.addColumn("titulo");
    modelo.addColumn("idioma");
    modelo.addColumn("3D");
    modelo.addColumn("Subtitulada");
    modelo.addColumn("HoraDeInicio");
    modelo.addColumn("HoraDeFin");
     modelo.addColumn("Sala");
     jtTabla.setModel(modelo);

}
    public void cargarTabla(){
    
        for (Funcion funci : funcionData.listarFunciones()) {
            modelo.addRow(new Object[]{
                funci.getIdFuncion(),
                funci.getTitulo(),
                funci.getIdioma(),
                funci.isEs3D(),
                funci.isSubtitulada(),
                funci.getHoraDeInicio(),
                funci.getHoraDeFin(),
                funci.getSala()
                
               
            
                    
                    });
            
        }
    
    
    }
